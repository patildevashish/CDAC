// WAP to print greater between two nos.
#include<iostream>
using namespace std;

int main()
{
    int a,b;
    cout<<"Enter any number: ";
    cin>>a>>b;
    if (a>b)
    {
        cout<<"1st number is greater";
    }
    else if (a==b)
    {
        cout<<"Both number are equal";
    }
    else cout<<"b is greater";
}

